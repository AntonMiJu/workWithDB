CREATE TABLE EMPLOYEES(
    ID NUMBER,
    CONSTRAINT EMPLOYEE_ID PRIMARY KEY (ID),
    LAST_NAME NVARCHAR2(50),
    FIRST_NAME NVARCHAR2(20),
    TITLE NVARCHAR2(20),
    TITLE_OF_COURTESY NVARCHAR2(20),
    BIRTH_DATE TIMESTAMP,
    HIRE_DATE TIMESTAMP,
    ADDRESS NVARCHAR2(50),
    CITY NVARCHAR2(20),
    REGION NVARCHAR2(60),
    POSTAL_CODE NUMBER,
    COUNTRY NVARCHAR2(20),
    HOME_PHONE NUMBER,
    EXTENSION NUMBER,
    PHOTO NVARCHAR2(500),
    NOTES CLOB,
    REPORTS_TO NUMBER,
    CONSTRAINT REPORT_FK FOREIGN KEY (REPORTS_TO) REFERENCES EMPLOYEES(ID)
);